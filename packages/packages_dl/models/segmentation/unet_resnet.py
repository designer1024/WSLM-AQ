class Net(nn.Module):
    def __init__(self,model_name='resnet50',input_channels=4,num_classes=1,pretrain_backbone=False, include_top=False):
        super(Net, self).__init__()
        self.num_classes=num_classes
        self.input_channes=input_channels
        self.resnet = globals()[model_name](num_classes=num_classes,input_channels=input_channels, include_top=include_top)
        self.stage0=nn.Sequential(self.resnet.conv1, self.resnet.bn1, self.resnet.relu)
        self.stage1 = nn.Sequential(self.resnet.maxpool,self.resnet.layer1)
        self.stage2 = nn.Sequential(self.resnet.layer2)
        self.stage3 = nn.Sequential(self.resnet.layer3)
        self.stage4 = nn.Sequential(self.resnet.layer4)
        self.up1=Up(2048+1024,1024)
        self.up2=Up(1024+512,512)
        self.up3=Up(512+256,256)
        self.up4=Up(256+64,64)
        self.conv = OutConv(64, num_classes=num_classes)

    def forward(self, inputs):
        input_shape = inputs.shape[-2:]
        x1_0_0 = self.stage0(inputs)
        x1_1_0 = self.stage1(x1_0_0)#conv1_0
        x1_2_0 = self.stage2(x1_1_0)#conv2_0
        x1_3_0 = self.stage3(x1_2_0)#conv3_0
        x1_4_0=self.stage4(x1_3_0)

        x=self.up1(x1_4_0,x1_3_0)
        x=self.up2(x,x1_2_0)
        x=self.up3(x,x1_1_0)
        x=self.up4(x,x1_0_0)
        x=self.conv(x)
        x = F.interpolate(x, size=input_shape, mode="bilinear", align_corners=False)
        return {'out':x}
    def trainable_parameters(self):
        params = filter(lambda p: p.requires_grad, self.parameters())
        return params


